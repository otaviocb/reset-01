public class Calculadora {

    int somar(int valorUm, int valorDois){
        return valorUm + valorDois;

    }
    int subtrair(int valorUm, int valorDois) {
        return valorUm - valorDois;
    }

    int dividir(int valorUm, int valorDois) {

        return valorUm / valorDois;
    }

    int multiplicar(int valorUm, int valorDois) {

        return valorUm * valorDois;
    }
}
